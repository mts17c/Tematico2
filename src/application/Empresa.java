package application;

public class Empresa {
	
	private String nome;
	private String CNPJ;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCNPJ() {
		return CNPJ;
	}
	public void setCNPJ(String cNPJ) {
		this.CNPJ = cNPJ;
	}
	
	
}
